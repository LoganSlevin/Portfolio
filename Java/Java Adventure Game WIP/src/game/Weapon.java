package game;

public class Weapon {

	private String name;
	private int damage;                                                                                                                                                                                                                                                                                                                                                                                                                                                 
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getDamage() {
		return damage;
	}
	public void setDamage(Integer damage) {
		this.damage = damage;
	}
	
	
	

}
